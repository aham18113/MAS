class ShooterAgent(Agent):
    def __init__(self, unique_id, model, pos, range):
        super().__init__(unique_id, model)
        self.pos = pos
        self.range = range
        self.trajectory = [pos]
        self.suppressed = False

    def step(self):
        if not self.suppressed:
            self.move()
            print(f"Shooter {self.unique_id} moves to position {self.pos}")
        else:
            print(f"Shooter {self.unique_id} is suppressed and cannot move.")

    def move(self):
        """Move the shooter randomly within the hallways (tile type 4), avoiding previously visited positions."""
        neighbors = self.get_hallway_neighbors(self.pos)
        unvisited_neighbors = [n for n in neighbors if n not in self.trajectory]

        if unvisited_neighbors:
            next_move = random.choice(unvisited_neighbors)
        else:
            next_move = random.choice(neighbors)

        self.model.grid.move_agent(self, next_move)
        self.pos = next_move
        self.trajectory.append(next_move)

    def get_hallway_neighbors(self, pos):
        """Return valid hallway neighbors (tile type 4) for movement."""
        x, y = pos
        neighbors = [(x + dx, y + dy) for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]]
        return [
            (nx, ny)
            for nx, ny in neighbors
            if 0 <= nx < self.model.width
            and 0 <= ny < self.model.height
            and self.model.custom_map[ny][nx] == 7
        ]

    def get_visible_range(self):
        visible_tiles = set()

        def cast_ray(start_pos, direction, max_range):
            x, y = start_pos
            dx, dy = direction
            for i in range(1, max_range + 1):
                nx = x + i * dx
                ny = y + i * dy

                if 0 <= nx < self.model.width and 0 <= ny < self.model.height:
                    if self.model.custom_map[int(ny)][int(nx)] == 1:
                        break
                    visible_tiles.add((int(nx), int(ny)))
                else:
                    break

        num_rays = 360
        for angle in range(0, num_rays, 1):
            radians = math.radians(angle)
            direction = (math.cos(radians), math.sin(radians))
            cast_ray(self.pos, direction, self.range)

        return visible_tiles

    def suppress(self):
        """Mark the shooter as suppressed."""
        self.suppressed = True
        print(f"Shooter {self.unique_id} has been suppressed by the police.")


class UnitAgent(Agent):
    def __init__(self, unique_id, model, pos, hiding=False):
        super().__init__(unique_id, model)
        self.pos = pos
        self.trajectory = [pos]
        self.path = []
        self.evacuated = False
        self.survival_probability = 1.0
        self.hiding = hiding

    def step(self):
        # Students will start hiding if within the hiding radius of the first death
        if self.model.first_student_killed and not self.evacuated:
            distance_to_first_death = self.model.manhattan_distance(
                self.pos, self.model.first_death_position
            )
            if distance_to_first_death <= self.model.hiding_radius:
                self.hiding = True

        # If hiding, stay in place
        if self.hiding:
            print(f"Agent {self.unique_id} is hiding at position {self.pos}")
            return

        # Students start moving if there are 2 or more kills, or after 15 steps have passed without any kills
        if self.model.students_killed_count < 1:
            print(f"Agent {self.unique_id} is waiting at position {self.pos}")
            return

        if not self.path:
            nearest_edge_tile = self.find_nearest_edge_tile()
            if nearest_edge_tile:
                self.path = self.a_star(self.pos, nearest_edge_tile)

        if self.path:
            next_step = self.path.pop(0)
            self.move_to(next_step)
            print(
                f"Time: {self.model.schedule.time * self.model.time_step} seconds: Agent {self.unique_id} at position {self.pos}"
            )

    def find_nearest_edge_tile(self):
        width, height = len(self.model.custom_map[0]), len(self.model.custom_map)
        edge_tiles = [
            (x, y)
            for x in range(width)
            for y in range(height)
            if (x == 0 or y == 0 or x == width - 1 or y == height - 1)
            and self.model.custom_map[y][x] == 2
        ]
        if edge_tiles:
            return min(
                edge_tiles, key=lambda tile: self.manhattan_distance(self.pos, tile)
            )
        return None

    def a_star(self, start, goal):
        """A* pathfinding algorithm to find the shortest path, avoiding shooter's visibility."""
        open_set = []
        heapq.heappush(open_set, (0, start))
        came_from = {}
        g_score = {start: 0}
        f_score = {start: self.manhattan_distance(start, goal)}

        dangerous_tiles = self.get_dangerous_tiles()

        while open_set:
            _, current = heapq.heappop(open_set)

            if current == goal:
                return self.reconstruct_path(came_from, current)

            for neighbor in self.get_neighbors(current):
                if neighbor in dangerous_tiles:  # Avoid dangerous tiles
                    continue

                tentative_g_score = g_score[current] + 1

                if neighbor not in g_score or tentative_g_score < g_score[neighbor]:
                    came_from[neighbor] = current
                    g_score[neighbor] = tentative_g_score
                    f_score[neighbor] = tentative_g_score + self.manhattan_distance(
                        neighbor, goal
                    )
                    if neighbor not in [i[1] for i in open_set]:
                        heapq.heappush(open_set, (f_score[neighbor], neighbor))

        return []

    def get_dangerous_tiles(self):
        """Combine visible ranges of all shooters to get dangerous tiles."""
        dangerous_tiles = set()
        for shooter in [
            agent
            for agent in self.model.schedule.agents
            if isinstance(agent, ShooterAgent)
        ]:
            dangerous_tiles.update(shooter.get_visible_range())
        return dangerous_tiles

    def reconstruct_path(self, came_from, current):
        path = []
        while current in came_from:
            path.append(current)
            current = came_from[current]
        path.reverse()
        return path

    def get_neighbors(self, pos):
        x, y = pos
        neighbors = [(x + dx, y + dy) for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]]
        return [
            (nx, ny)
            for nx, ny in neighbors
            if 0 <= nx < self.model.width
            and 0 <= ny < self.model.height
            and (
                self.model.custom_map[ny][nx] == 3
                or self.model.custom_map[ny][nx] == 2
                or self.model.custom_map[ny][nx] == 4
                or self.model.custom_map[ny][nx] == 5
                or self.model.custom_map[ny][nx] == 6
                or self.model.custom_map[ny][nx] == 7
            )
        ]

    def manhattan_distance(self, pos1, pos2):
        """Heuristic for A* (Manhattan distance)."""
        return abs(pos1[0] - pos2[0]) + abs(pos1[1] - pos2[1])

    def move_to(self, next_pos):
        """Move the agent to the next position on the path."""
        self.model.grid.move_agent(self, next_pos)
        self.pos = next_pos
        self.trajectory.append(next_pos)

        if (
            self.pos[0] == 0
            or self.pos[1] == 0
            or self.pos[0] == self.model.width - 1
            or self.pos[1] == self.model.height - 1
        ):
            self.evacuated = True


class PoliceAgent(Agent):
    def __init__(self, unique_id, model, pos, range, delay_steps):
        super().__init__(unique_id, model)
        self.pos = pos
        self.range = range
        self.trajectory = [pos]
        self.delay_steps = delay_steps
        self.steps_since_entry = 0
        self.path = []  # Store path to the shooter's position

    def step(self):
        """Police agent logic executed every step."""
        if self.steps_since_entry >= self.delay_steps:
            self.chase_shooter()
            self.check_for_shooter()
            self.check_for_students()
        self.steps_since_entry += 1

    def chase_shooter(self):
        """Calculate path to the shooter and move along it."""
        shooter = self.get_shooter()
        if shooter:
            # Recalculate the path only if the shooter has moved
            if not self.path or self.path[-1] != shooter.pos:
                self.path = self.a_star(self.pos, shooter.pos)

            if self.path:
                next_step = self.path.pop(0)
                self.move_to(next_step)

    def get_shooter(self):
        """Retrieve the single shooter agent."""
        for agent in self.model.schedule.agents:
            if isinstance(agent, ShooterAgent):
                return agent
        return None

    def a_star(self, start, goal):
        """A* pathfinding algorithm to find the shortest path to the shooter."""
        open_set = []
        heapq.heappush(open_set, (0, start))
        came_from = {}
        g_score = {start: 0}
        f_score = {start: self.model.manhattan_distance(start, goal)}

        while open_set:
            _, current = heapq.heappop(open_set)

            if current == goal:
                return self.reconstruct_path(came_from, current)

            for neighbor in self.get_hallway_neighbors(current):
                tentative_g_score = g_score[current] + 1

                if neighbor not in g_score or tentative_g_score < g_score[neighbor]:
                    came_from[neighbor] = current
                    g_score[neighbor] = tentative_g_score
                    f_score[neighbor] = (
                        tentative_g_score
                        + self.model.manhattan_distance(neighbor, goal)
                    )
                    if neighbor not in [i[1] for i in open_set]:
                        heapq.heappush(open_set, (f_score[neighbor], neighbor))

        return []

    def reconstruct_path(self, came_from, current):
        """Reconstruct the path from the A* algorithm."""
        path = []
        while current in came_from:
            path.append(current)
            current = came_from[current]
        path.reverse()
        return path

    def get_hallway_neighbors(self, pos):
        """Return valid hallway neighbors for movement."""
        x, y = pos
        neighbors = [(x + dx, y + dy) for dx, dy in [(-1, 0), (1, 0), (0, -1), (0, 1)]]
        return [
            (nx, ny)
            for nx, ny in neighbors
            if 0 <= nx < self.model.width
            and 0 <= ny < self.model.height
            and (
                self.model.custom_map[ny][nx] == 5 or self.model.custom_map[ny][nx] == 7
            )  # Hallway or entry tiles
        ]

    def move_to(self, next_pos):
        """Move the policeman to the next position."""
        self.model.grid.move_agent(self, next_pos)
        self.pos = next_pos
        self.trajectory.append(next_pos)

    def check_for_shooter(self):
        """Check if the shooter is in range to suppress them."""
        shooter = self.get_shooter()
        if (
            shooter
            and self.model.manhattan_distance(self.pos, shooter.pos) <= self.range / 2
        ):
            self.suppress_shooter(shooter)

    def suppress_shooter(self, shooter):
        """Suppress the shooter."""
        print(
            f"Police {self.unique_id} suppressed Shooter {shooter.unique_id} at position {shooter.pos}."
        )
        shooter.suppress()

    def check_for_students(self):
        """Check if any students are in range for evacuation."""
        for student in [
            agent
            for agent in self.model.schedule.agents
            if isinstance(agent, UnitAgent)
        ]:
            if (
                self.model.manhattan_distance(self.pos, student.pos) <= self.range
                and not student.evacuated
            ):
                self.evacuate_student(student)

    def evacuate_student(self, student):
        """Directly evacuate the student."""
        student.evacuated = True
        print(
            f"Police {self.unique_id} evacuated Student {student.unique_id} from position {student.pos}."
        )


class CustomMapModel(Model):
    def __init__(
        self,
        custom_map,
        num_students,
        num_shooter,
        num_police,
        time_step=6.0,
        initial_positions=None,
        police_delay=50,
    ):
        super().__init__()
        self.custom_map = custom_map
        self.width = len(custom_map[0])
        self.height = len(custom_map)
        self.grid = MultiGrid(self.width, self.height, torus=False)
        self.schedule = RandomActivation(self)
        self.casualties = 0
        self.death_positions = []
        self.students_in_range = set()
        self.time = 0
        self.first_student_killed = False
        self.students_killed_count = 0
        self.steps_since_start = 0
        self.time_step = time_step
        self.first_death_position = None
        self.hiding_radius = 20
        self.initial_positions = initial_positions  # Store initial positions
        self.place_units(num_students)
        self.place_shooters(num_shooter)
        self.place_police(num_police, police_delay)

    def place_police(self, num_police, delay_steps):
        """Place police officers on the map."""
        if self.initial_positions:
            police_positions = self.initial_positions["police"]
        else:
            exit_tiles = [
                (x, y)
                for x in range(self.width)
                for y in range(self.height)
                if self.custom_map[y][x] == 5
            ]  # Police enter from exit
            police_positions = random.sample(exit_tiles, num_police)

        for pos in police_positions:
            police = PoliceAgent(
                self.next_id(), self, pos, range=40, delay_steps=delay_steps
            )
            self.grid.place_agent(police, pos)
            self.schedule.add(police)

    def place_units(self, num_students):
        if self.initial_positions:
            start_positions = self.initial_positions["students"]
        else:
            passable_tiles = [
                (x, y)
                for x in range(self.width)
                for y in range(self.height)
                if self.custom_map[y][x] == 3
            ]
            start_positions = random.sample(passable_tiles, num_students)

        num_hiders = int(0 * num_students)
        hiding_students = random.sample(range(num_students), num_hiders)

        for i, pos in enumerate(start_positions):
            hiding = i in hiding_students
            unit = UnitAgent(self.next_id(), self, pos, hiding=hiding)
            self.grid.place_agent(unit, pos)
            self.schedule.add(unit)

    def place_shooters(self, num_shooter):
        if self.initial_positions:  # Use stored shooter position
            shooter_positions = self.initial_positions["shooter"]
        else:
            hallway_tiles = [
                (x, y)
                for x in range(self.width)
                for y in range(self.height)
                if self.custom_map[y][x] == 7
            ]
            shooter_positions = random.sample(hallway_tiles, num_shooter)

        for pos in shooter_positions:
            shooter = ShooterAgent(self.next_id(), self, pos, range=40)
            self.grid.place_agent(shooter, pos)
            self.schedule.add(shooter)

    def get_initial_positions(self):
        student_positions = [
            agent.pos for agent in self.schedule.agents if isinstance(agent, UnitAgent)
        ]
        shooter_positions = [
            agent.pos
            for agent in self.schedule.agents
            if isinstance(agent, ShooterAgent)
        ]
        return {"students": student_positions, "shooter": shooter_positions}

    def manhattan_distance(self, pos1, pos2):
        return abs(pos1[0] - pos2[0]) + abs(pos1[1] - pos2[1])

    def step(self):
        self.steps_since_start += 1
        self.time += self.time_step

        for shooter in [
            agent for agent in self.schedule.agents if isinstance(agent, ShooterAgent)
        ]:
            visible_range = shooter.get_visible_range()
            for student in [
                agent for agent in self.schedule.agents if isinstance(agent, UnitAgent)
            ]:
                if student.evacuated:
                    continue
                if student.pos in visible_range:
                    distance = self.manhattan_distance(shooter.pos, student.pos)
                    probability = calculate_shooting_probability(
                        distance, shooter.range
                    )
                    student.survival_probability = 1 - probability
                    if probability > 0.6:
                        self.remove_student(student)
                    else:
                        self.students_in_range.add(student.unique_id)
        self.schedule.step()

    def remove_student(self, student):
        """Remove a student and log their death."""
        self.students_killed_count += 1  # Increment the number of student deaths

        if not self.first_student_killed and self.students_killed_count == 1:
            self.first_student_killed = True
            self.first_death_position = student.pos
            print(
                f"First student killed at position {student.pos}. Students nearby will now hide."
            )

        self.death_positions.append(student.pos)
        self.grid.remove_agent(student)
        self.schedule.remove(student)
        self.casualties += 1
        print(
            f"Student {student.unique_id} has been shot at {student.pos}. Total casualties: {self.casualties}"
        )

    def get_trajectories(self):
        return {
            agent.unique_id: agent.trajectory
            for agent in self.schedule.agents
            if isinstance(agent, UnitAgent)
        }

    def calculate_metrics(self):
        total_students = (
            len(
                [
                    agent
                    for agent in self.schedule.agents
                    if isinstance(agent, UnitAgent)
                ]
            )
            + self.casualties
        )
        evacuated_students = len(
            [
                agent
                for agent in self.schedule.agents
                if isinstance(agent, UnitAgent) and agent.evacuated
            ]
        )
        casualty_percentage = (self.casualties / total_students) * 100
        evacuation_efficiency = (evacuated_students / total_students) * 100
        return casualty_percentage, evacuation_efficiency


def calculate_shooting_probability(distance, max_range):
    return max(0, 1 - (distance / max_range))



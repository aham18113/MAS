def run_simulation_n_times(
    custom_map, num_students, num_shooters, num_police, n, time_step=6.0
):
    total_casualties = 0
    total_evacuated = 0

    for i in range(n):
        print(f"Running simulation iteration {i + 1}")
        initial_positions = generate_initial_positions(
            custom_map, num_students, num_police, num_shooters, seed=i + 2
        )

        model = CustomMapModel(
            custom_map,
            num_students,
            num_shooters,
            num_police,
            time_step,
            initial_positions,
        )

